

public class CheckNegativeNumber {

    public static void main(String[] args) {
        int number = 7;

        
        if (number < 0) {
            System.out.println(number + " is a negative number.");
        } else if (number > 0) {
            System.out.println(number + " is a positive number.");
        } else {
            System.out.println(number + " is zero.");
        }

       
    }
}
